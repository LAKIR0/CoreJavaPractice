class DataTypeDemo
{
	public static void main(String args[])
	{
		int a =1;
		float b=1.3f;
		boolean pass = true;
		char c ='h';
		System.out.println("examples of variables");
		System.out.println("int = "+a);
		System.out.println("float = "+b);
		System.out.println("boolean = "+pass);
		System.out.println("char = "+c);
	}
}
	